#include<bits/stdc++.h>
using namespace std;
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;
    while(t--)
    {
        int n;
        cin >> n;
        string s;
        cin >> s;
        bool zero = false;
        int cnt1 = 0;
        for(int i=0; i<n; i++)
        {
            if(s[i] == '0')
               zero = true;
            else if(zero && s[i] == '1')
               cnt1++;
          
        }
        cout << cnt1 << "\n";
    }

    return 0;
}